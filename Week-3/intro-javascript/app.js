var num1 = 10;
var num2 = 2;
console.log("num1 = ", num1);
console.log("num2 = " + num2);

// addition
var add = num1 + num2;
console.log("addition: " + num1 + " + " + num2 + " = " + add);

// subtraction
var sub = num1 - num2;
console.log("subtraction:" + num1 + " - " + num2 + " = " + sub);

// multiplication
var mul = num1 * num2;
console.log("multiplication: " + num1 + " * " + num2 + " = " + mul);

// division
var div = num1 / num2;
console.log("division: " + num1 + " / " + num2 + " = " + div);
